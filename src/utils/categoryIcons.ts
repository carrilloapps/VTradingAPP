/**
 * Get icon name for a given category
 * Performs exact match first, then partial match, then returns default
 */
export function getCategoryIcon(categoryName: string): string {
  // Exact match
  if (CATEGORY_ICONS[categoryName]) {
    return CATEGORY_ICONS[categoryName];
  }

  // Partial match (case insensitive)
  const lowerCategoryName = categoryName.toLowerCase();
  const matchedKey = Object.keys(CATEGORY_ICONS).find(
    key =>
      lowerCategoryName.includes(key.toLowerCase()) ||
      key.toLowerCase().includes(lowerCategoryName),
  );

  return matchedKey ? CATEGORY_ICONS[matchedKey] : 'tag';
}

/**
 * Get all available categories (sorted alphabetically)
 */
export function getAllCategories(): string[] {
  return Object.keys(CATEGORY_ICONS).sort();
}

/**
 * Search categories by term
 */
export function searchCategories(searchTerm: string): string[] {
  const lowerSearch = searchTerm.toLowerCase();
  return Object.keys(CATEGORY_ICONS)
    .filter(cat => cat.toLowerCase().includes(lowerSearch))
    .sort();
}
